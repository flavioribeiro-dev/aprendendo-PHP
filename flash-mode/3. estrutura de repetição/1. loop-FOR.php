<?php

// Loop FOR
for($idade=10; $idade<=18; $idade++) {
    echo $idade."<br>";
}

for($num=0; $num>-15; $num--) {
    echo $num."<br>";
}


// --- Receber um número N e exibir todos os números de 1 a N:
$numInicio = 5;
$numExemplo = 10;
for($numInicio; $numInicio<=$numExemplo; $numInicio++) {
    echo $numInicio."<br>";
}