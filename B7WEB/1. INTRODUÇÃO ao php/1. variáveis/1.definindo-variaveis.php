<?php

// Definição de Variáveis utilizando o $:
$nome = "Flávio";
$sobrenome = "Ribeiro";
$idade = 90;

// Concatenação de Strings:
echo("Meu nome é $nome $sobrenome e eu tenho $idade anos <br>"); 
echo('Meu nome é '.$nome.' '.$sobrenome.' e eu tenho '.$idade.' anos');
// --- A Concatenação com ASPAS SIMPLES exige a utilização de PONTO e a COMPENSAÇÃO DO ESPAÇO EM BRANCO no encontro do texto com cada Variável.
